CREATE DATABASE AgenciaBancariaAline;

GO 

USE AgenciaBancariaAline;

GO

CREATE TABLE CLIENTE 
(
	CPF BIGINT NOT NULL,
	NOME VARCHAR(250) NOT NULL,
	DATA_NASCIMENTO DATE NOT NULL,
	
	CONSTRAINT PK_CLIENTE PRIMARY KEY (CPF),
);

GO

CREATE TABLE CONTA
(
	NUMERO BIGINT NOT NULL,
	DIGITO INT NOT NULL,
	AGENCIA VARCHAR(5) NOT NULL,
	TIPO_DE_CONTA INT NOT NULL,
	SALDO DECIMAL(11,2) NOT NULL,
	LIMITE DECIMAL(11,2),
	DATA_ABERTURA DATE NOT NULL,
	CESTA INT,
	CPF_CLIENTE BIGINT,

	CONSTRAINT PK_CONTA PRIMARY KEY (NUMERO),
	CONSTRAINT FK_CLIENTE_CONTA FOREIGN KEY (CPF_CLIENTE) REFERENCES CLIENTE,
);

GO

